# Variabele
a = "Wat is de hoofdstad van Frankrijk?"
# Input
b = input(a)
# Nog een variabele
c = "Parijs"
# If
if c == b:
    # Wanneer het antwoord juist is wordt er "juist!" geprint op het scherm van de gebruiker. Zo weet de gebruiker dat hij een vraag juist had.
    print("Juist!")
else:
    print("Fout!")